Friends = ["sandy", "oscar", "doggo", "punk", "bored", "ape"]

# we can use int, string, boolean etc in lists , it will not show us error

print(Friends)
print(Friends[0])
print(Friends[1])
print(Friends[3])

print(Friends[-1])
print(Friends[-2])
print(Friends[-3])

print(Friends[1:])   # it will grab all the elements from index position 1 till the end
print(Friends[1:5])  # it will grab all the elements from index position 1 to 4 ( excluding 5 )

Friends[1] = "pug"   # we can also update the values in lists like this
print(Friends[1])
print(Friends)       # updated list











